# Write a program that:
# • asks the user to input a sentence
# • asks the user to input:
# • the word they want to replace
# • the word they want to replace it with
# • outputs the new sentence

sentence  = input("Enter any sentence you would like : ")
word1 = input("Enter word you wish to replace : ")
word2 = input("Enter word to replace with : ")

print(sentence.replace(word1, word2))



